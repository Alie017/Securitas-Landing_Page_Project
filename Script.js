               
                        var Y = document.getElementsByClassName("templateSplash");
                        
function butn() {

  for (let x of Y) {
    if (x.style.display === "none") {
      x.style.display = "flex";
    
    } else {
      x.style.display = "none";
    }
  }
                        }



